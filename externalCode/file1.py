import math  

def addition(a, b):
    return a + b

def sub(a, b):
    return a - b

def mult(a, b):
    return a * b

def division(a, b):
    if b != 0:
        return a / b
    else:
        return "Cannot divide by zero."

def pow(a, b):
    return math.pow(a, b)

def sqrt(a):
    return math.sqrt(a)

